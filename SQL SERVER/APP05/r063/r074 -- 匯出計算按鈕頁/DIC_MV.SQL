SELECT
	T1.CURRENT_CODE AS ENTITY_CODE,
	T1.SCENARIO,
	--有些月份是06有些是6，統一格式
	 CONCAT(T1.PERIOD,'-', RIGHT('0' + T1.MONTH,2)) AS PERIOD,
	SUM(T1.ACCUMULATED_AMOUNT_ORI * -1) AS ACCUMULATED_AMOUNT 
FROM MV_TRS_TB_ACTUAL_ACCOUNT_AMOUNT T1
JOIN TRS_DIM_ENTITY t2 ON t1.ENTITY_CODE = t2.ENTITY_CODE AND T2.COUNTRY_ID != 'TW'
LEFT JOIN V_TRS_FACT_EXCHANGE_BANKOFTW T3 ON CONCAT(T1.PERIOD,'-', RIGHT('0' + T1.MONTH,2)) = T3.PERIOD AND T1.CURRENCY_CODE = T3.CURRENCY_ID AND TARGET_CURRENCY_ID = 'NTD' AND T1.SCENARIO = T3.SCENARIO 
WHERE SUBSTRING(T2_CODE, 1, 1) BETWEEN '4' AND '7' 
AND  CONCAT(T1.PERIOD,'-', RIGHT('0' + T1.MONTH,2))= '${P_DATE}'
AND t1.SCENARIO = '${P_VERSION}'
GROUP BY T1.CURRENT_CODE, T1.SCENARIO,  CONCAT(T1.PERIOD,'-', RIGHT('0' + T1.MONTH,2))