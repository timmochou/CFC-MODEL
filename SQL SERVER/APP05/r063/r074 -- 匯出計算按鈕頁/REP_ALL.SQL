SELECT
	t1.ENTITY_CODE,
    t2.COMPANY_NAME,
    t1.INV_ENTITY_CODE,
	t1.PERIOD,
--	t1.SCENARIO,
	t1.ACCUMULATED_AMOUNT,
--	t1.POS_RECOGNIZED_AMOUNT,
--	t1.NEG_RECOGNIZED_AMOUNT,
	t1.AMOUNT_TOTAL,
	t1.REALIZED_LOSS,
	t1.DISTRI_NONCN,
	t1.DISTRI_CN,
	t1.LIMIT_ITEMS,
	t1.DESPITE_PROFIT,
	t1.FVPL_FVCHANGE,
	t1.FVPL_AMOUNT,
	t1.RE_FVPL_AMOUNT,
	t1.NON_CFC_DISPOSAL_ADJ,
	t1.CFC_EARNINGS,
--	t1.TAX_INC_BEFORE_RATIO,
	t1.DEDUCTION,
	t1.RATIO_HOLD,
	t1.FVPL_APP,
	t1.TAX_INC
FROM V_TRS_FACT_CFC_TAX_INCOME_TOTAL t1
LEFT JOIN V_TRS_DIM_COMPANY t2
ON t1.ENTITY_CODE = t2.COMPANY_CODE
WHERE t2.LANGUAGE = '${fr_locale}'
AND 1 = 1
--	AND IS_CFC = 'true'
	AND t1.ENTITY_CODE = '${P_COMPANY}'
-- AND t1.INV_ENTITY_CODE = '${P_CFC}'
	AND FORMAT(t1.PERIOD, 'yyyy-MM') = '${P_DATE}'
	AND t1.SCENARIO = '${P_VERSION}'